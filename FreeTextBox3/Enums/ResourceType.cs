using System;

namespace FreeTextBoxControls {
	/// <summary>
	/// The images used to draw FreeTextBox
	/// </summary>
	public enum ResourceType {
		/// <summary>
		/// An image used for <see cref="ToolbarButton"/>
		/// </summary>
		Button = 0,
		/// <summary>
		/// Miscellaneous images used to draw the FreeTextBox toolbars
		/// </summary>
		Toolbar = 1,
		/// <summary>
		/// The scripts used in FreeTextBox
		/// </summary>
		JavaScript = 2,
		/// <summary>
		/// Image files used in non-button functions
		/// </summary>
		Utility = 3
	}
}
